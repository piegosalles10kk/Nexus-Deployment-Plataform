// ── POST /api/v1/agent/nodes ──────────────────────────────────────────────────
/**
 * Registers a new node and returns a short-lived JWT enrollment token
 * that the install script will use to call /enroll.
 */
export async function createNode(req: Request, res: Response, next: NextFunction) {
  try {
    const { name } = req.body as { name?: string };
    if (!name || name.trim().length === 0) {
      res.status(400).json({ status: 'error', message: '"name" is required.' });
      return;
    }

    // Permanent token stored in DB — used to match the node on reconnect
    const persistentToken = crypto.randomBytes(32).toString('hex');

    const node = await prisma.node.create({
      data: {
        name:   name.trim(),
        os:     'unknown',
        arch:   'unknown',
        token:  persistentToken,
        status: 'OFFLINE',
      },
    });

    // Short-lived JWT for the install script (1 hour)
    const enrollToken = jwt.sign(
      { nodeId: node.id, purpose: 'enroll' },
      env.JWT_SECRET,
      { expiresIn: '1h' },
    );

    res.status(201).json({
      status: 'success',
      data: { node, enrollToken },
    });
  } catch (error) {
    next(error);
  }
}

// ── DELETE /api/v1/agent/nodes/:id ───────────────────────────────────────────
export async function deleteNode(req: Request<{ id: string }>, res: Response, next: NextFunction) {
  try {
    const { id } = req.params;
    const ws = getAgentSocket(id);
    if (ws) ws.close(1000, 'Node deleted');

    await prisma.node.delete({ where: { id } });
    res.json({ status: 'success', message: 'Node deleted.' });
  } catch (error) {
    next(error);
  }
}

// ── POST /api/v1/agent/nodes/:id/command ─────────────────────────────────────
/**
 * Sends a JSON command to a connected agent (e.g. stream_logs, update).
 */
export async function sendCommand(
  req: Request<{ id: string }>,
  res: Response,
  next: NextFunction,
) {
  try {
    const { id } = req.params;
    const ws = getAgentSocket(id);

    if (!ws || ws.readyState !== ws.OPEN) {
      res.status(409).json({ status: 'error', message: 'Agent is not connected.' });
      return;
    }

    ws.send(JSON.stringify(req.body));
    res.json({ status: 'success', message: 'Command dispatched.' });
  } catch (error) {
    next(error);
  }
}

// ── GET /api/v1/agent/nodes/:id/telemetry ────────────────────────────────────
/**
 * Fetches the historical telemetry data (last 100 entries) from Redis.
 */
export async function getNodeTelemetry(req: Request<{ id: string }>, res: Response, next: NextFunction) {
  try {
    const { id } = req.params;
    const redis = await getRedisClient();
    const key = `node:${id}:telemetry`;
    
    // Fetch the 100 entries. They are stored as strings.
    const rawData = await redis.lRange(key, 0, 99);
    
    // Parse strings and return in chronological order (LPUSH stores newest at index 0, so we reverse it)
    const telemetry = rawData.map(str => {
      try { return JSON.parse(str); } catch { return null; }
    }).filter(Boolean).reverse();

    res.json({ status: 'success', data: { telemetry } });
  } catch (error) {
    next(error);
  }
}

// ── GET /api/v1/agent/nodes/:id/scan-ports ──────────────────────────────────
/**
 * Requests a real-time port scan from a connected agent.
 */
export async function scanNodePorts(req: Request<{ id: string }>, res: Response, next: NextFunction) {
  try {
    const { id } = req.params;
    const ports = await requestPortScan(id);
    res.json({ status: 'success', data: { ports } });
  } catch (error) {
    // If agent is offline or timeout occurs
    res.status(502).json({ status: 'error', message: error instanceof Error ? error.message : 'Port scan failed' });
  }
}

