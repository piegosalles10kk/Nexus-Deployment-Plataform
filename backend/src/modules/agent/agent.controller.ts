// ── POST /api/v1/agent/enroll ─────────────────────────────────────────────────
/**
 * Validates the provisioning token, issues mTLS client certificates,
 * and returns them in a JSON payload for the install script.
 *
 * The provisioning token is a JWT with claim { nodeId, purpose: "enroll" }
 * generated by createNode() below.
 */
export async function enrollAgent(req: Request, res: Response, next: NextFunction) {
  try {
    const authHeader = req.headers['authorization'] ?? '';
    const raw = authHeader.startsWith('Bearer ') ? authHeader.slice(7) : '';
    if (!raw) {
      res.status(401).json({ status: 'error', message: 'Enrollment token required.' });
      return;
    }

    let decoded: any;
    try {
      decoded = jwt.verify(raw, env.JWT_SECRET);
    } catch {
      res.status(401).json({ status: 'error', message: 'Invalid or expired enrollment token.' });
      return;
    }

    if (decoded.purpose !== 'enroll' || !decoded.nodeId) {
      res.status(401).json({ status: 'error', message: 'Token is not a valid enrollment token.' });
      return;
    }

    const node = await prisma.node.findUnique({ where: { id: decoded.nodeId } });
    if (!node) {
      res.status(404).json({ status: 'error', message: 'Node not found.' });
      return;
    }

    // Issue a long-lived JWT the agent will use for all future WS connections.
    // The WS server authenticates agents with jwt.verify(), so we need a proper
    // JWT here — the raw node.token (random hex) cannot be verified that way.
    const agentToken = jwt.sign(
      { nodeId: node.id },
      env.JWT_SECRET,
      // No expiry — the token is tied to this node and revoked by deleting the node.
    );

    res.json({ status: 'success', data: { nodeToken: agentToken } });
  } catch (error) {
    next(error);
  }
}

